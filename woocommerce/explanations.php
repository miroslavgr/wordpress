
Zone = name ( admin side only ) + regions + list shipping methods

Order of zones matters for matching their address, thats why the last uneditable zone is for locations not covered by the others. 
So the user will lastly go to that. 

Shipping methods
shipping method = name( for customers ) + bool taxable + cost
There are 3 core shipping methods for use in the WP Core
1. Local Pickup 
2. Flat Rate
3. Free Shipping - can require min amount and/or coupon for it.

Shipping classes are grouping of proudcts, which can be applied different shipping rates later on.
